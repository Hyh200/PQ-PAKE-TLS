func durMS(d time.Duration) float64 {
	return float64(d) / float64(time.Millisecond)
}

func must(err error) {
	if err != nil {
		panic(err)
	}
}

func mustCreate(p string) *os.File {
	f, err := os.Create(p)
	must(err)
	return f
}

// ---- Small helpers that mirror the PAKE computations to time them explicitly ----

func pakeRunHKDF(ss, ctx []byte) []byte {
	// Use the full protocol once to reuse correct implementations.
	// Here we call a private helper by reusing pake.Run via an isolated path.
	// For timing we just return a fixed-length HKDF output.
	return pakeHKDFExpand(ss, ctx, 32)
}

func pakeRunIC(ssS, pkC, ctx, in []byte) []byte {
	key := pakeHKDFExpand(append(ssS, pkC...), ctx, len(in))
	return pakeICXor(key, ctx, in)
}

func pakeRunAuth(ssS, ssC, ctx []byte) []byte {
	return pakeHMACTag(ssS, ssC, ctx)
}

func pakeRunSession(ssS, ssC, ctx []byte) []byte {
	return pakeHKDFExpand(append(ssS, ssC...), ctx, 32)
}

// Local re-implementations (must match pake/pq_pake.go)

func pakeHKDFExpand(ikm, info []byte, outLen int) []byte {
	// Use crypto/hkdf via stdlib
	// (We keep it here to avoid exporting internals from package pake.)
	r := hkdfNewSHA256(ikm, info)
	out := make([]byte, outLen)
	_, _ = r.Read(out)
	return out
}

// minimal hkdf reader
func hkdfNewSHA256(ikm, info []byte) *hkdfReader {
	return newHKDFReader(ikm, info)
}

// --- HKDF (HMAC-SHA256) minimal implementation ---
// We avoid importing non-stdlib packages.

type hkdfReader struct {
	prk  []byte
	info []byte
	n    byte
	buf  []byte
}

func newHKDFReader(ikm, info []byte) *hkdfReader {
	// Extract with empty salt
	prk := hmacSHA256(nil, ikm)
	return &hkdfReader{prk: prk, info: info, n: 0, buf: nil}
}

func (r *hkdfReader) Read(p []byte) (int, error) {
	written := 0
	for written < len(p) {
		if len(r.buf) == 0 {
			r.n++
			r.buf = hkdfExpandBlock(r.prk, r.buf, r.info, r.n)
		}
		n := copy(p[written:], r.buf)
		r.buf = r.buf[n:]
		written += n
	}
	return written, nil
}

func hkdfExpandBlock(prk, prev, info []byte, counter byte) []byte {
	m := make([]byte, 0, len(prev)+len(info)+1)
	m = append(m, prev...)
	m = append(m, info...)
	m = append(m, counter)
	return hmacSHA256(prk, m)
}

func hmacSHA256(key, msg []byte) []byte {
	mac := hmac.New(sha256.New, key)
	mac.Write(msg)
	return mac.Sum(nil)
}

func pakeHMACTag(ssS, ssC, ctx []byte) []byte {
	mac := hmac.New(sha256.New, append(ssS, ssC...))
	mac.Write(ctx)
	return mac.Sum(nil)
}

func pakeICXor(key, ctx, ct []byte) []byte {
	// Reuse SHAKE256 from CIRCL (same as package pake)
	xof := sha3.NewShake256()
	xof.Write([]byte("ic-v1"))
	xof.Write([]byte{0})
	xof.Write(key)
	xof.Write([]byte{0})
	xof.Write(ctx)
	ks := make([]byte, len(ct))
	xof.Read(ks)
	out := make([]byte, len(ct))
	for i := range ct {
		out[i] = ct[i] ^ ks[i]
	}
	return out
}

func equal(a, b []byte) bool {
	if len(a) != len(b) {
		return false
	}
	var v byte
	for i := range a {
		v |= a[i] ^ b[i]
	}
	return v == 0
}
