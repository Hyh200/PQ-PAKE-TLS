type Result struct {
	ServerPK time.Duration
	ClientPK time.Duration
	ServerSK time.Duration
	ClientSK time.Duration
}

func main() {
	iters := flag.Int("iters", 1000, "number of PAKE sessions")
	pwStr := flag.String("pw", "password", "password used for client key derivation")
	outDir := flag.String("out", "out", "output directory")
	flag.Parse()

	ctx := []byte("ctx")
	pw := []byte(*pwStr)

	kemS := kemchcca.NewMLKEM768CHCCA()
	kemC := kempca.NewPWKyberPKEKEM()

	var acc Result
	for i := 0; i < *iters; i++ {
		// --- Server PK gen ---
		t0 := time.Now()
		pkS, skS, err := kemS.KeyGen()
		must(err)
		acc.ServerPK += time.Since(t0)

		// --- Client PK gen (pw-derived) ---
		t1 := time.Now()
		pkC, skC, err := kemC.DeriveKeyPairFromPassword(pw)
		must(err)
		acc.ClientPK += time.Since(t1)

		// --- Client side: Encap_s + K1 ---
		t2 := time.Now()
		ctS, ssS_client, err := kemS.Encap(pkS)
		must(err)
		_ = ssS_client
		// K1 (discarded in benchmark, but we include it in shared-key pipeline)
		_ = pakeRunHKDF(ssS_client, ctx)
		acc.ClientSK += time.Since(t2)

		// --- Server side: Decap_s + Encap_c + IC/HMAC/HKDF ---
		t3 := time.Now()
		ssS_server, err := kemS.Decap(skS, ctS)
		must(err)
		_ = pakeRunHKDF(ssS_server, ctx)
		ctC, ssC_server, err := kemC.Encap(pkC)
		must(err)
		eC := pakeRunIC(ssS_server, pkC, ctx, ctC)
		AuthS := pakeRunAuth(ssS_server, ssC_server, ctx)
		K_server := pakeRunSession(ssS_server, ssC_server, ctx)
		_ = K_server
		acc.ServerSK += time.Since(t3)

		// --- Client side: IC.Dec + Decap_c + HMAC + HKDF ---
		t4 := time.Now()
		ctC2 := pakeRunIC(ssS_client, pkC, ctx, eC) // xor again to decrypt
		ssC_client, err := kemC.Decap(skC, ctC2)
		must(err)
		AuthC := pakeRunAuth(ssS_client, ssC_client, ctx)
		if !equal(AuthC, AuthS) {
			panic("auth mismatch")
		}
		K_client := pakeRunSession(ssS_client, ssC_client, ctx)
		_ = K_client
		acc.ClientSK += time.Since(t4)
	}

	avg := Result{
		ServerPK: acc.ServerPK / time.Duration(*iters),
		ClientPK: acc.ClientPK / time.Duration(*iters),
		ServerSK: acc.ServerSK / time.Duration(*iters),
		ClientSK: acc.ClientSK / time.Duration(*iters),
	}

	must(os.MkdirAll(*outDir, 0o755))
	writeCSV(*outDir, avg)
	writeLaTeXTable(*outDir, avg)
	writePGFPlots(*outDir, avg)

	fmt.Printf("Average over %d runs:\n", *iters)
	fmt.Printf("  ServerPK: %v\n", avg.ServerPK)
	fmt.Printf("  ClientPK: %v\n", avg.ClientPK)
	fmt.Printf("  ServerSK: %v\n", avg.ServerSK)
	fmt.Printf("  ClientSK: %v\n", avg.ClientSK)
}

func writeCSV(outDir string, r Result) {
	f := mustCreate(filepath.Join(outDir, "results.csv"))
	defer f.Close()
	w := csv.NewWriter(f)
	_ = w.Write([]string{"metric", "avg_ms"})
	_ = w.Write([]string{"ServerPK-time", fmt.Sprintf("%.6f", durMS(r.ServerPK))})
	_ = w.Write([]string{"ClientPK-time", fmt.Sprintf("%.6f", durMS(r.ClientPK))})
	_ = w.Write([]string{"ServerSK-time", fmt.Sprintf("%.6f", durMS(r.ServerSK))})
	_ = w.Write([]string{"ClientSK-time", fmt.Sprintf("%.6f", durMS(r.ClientSK))})
	w.Flush()
}

